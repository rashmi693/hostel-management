private boolean check()
{
    Pattern ptrmail=Pattern.compile("^(.+)@(.+)$");
    Pattern ptrname=Pattern.compile("^[\\p{L} .'-]+$");
    Pattern ptrnumber=Pattern.compile("\\d{10}");
    Pattern ptrroom=Pattern.compile("\\d{3}");
    Pattern ptr=Pattern.compile("\\d{4,7}");
    Pattern ptrdob=Pattern.compile("^[0-3]?[0-9]/[0-3]?[0-9]/(?:[0-9]{2})?[0-9]{2}$");
    if(!ptrname.matcher(sname.getText()).matches())
    { JOptionPane.showMessageDialog(this,"Error in Student Name,(3-30)letters allowed","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrname.matcher(smname.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Mother's Name,(3-30)letters allowed","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrname.matcher(sfname.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Father's Name,(3-30)letters allowed","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrmail.matcher(smail.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in EmailId,(eg. xxx@gmail.com)","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrroom.matcher(srno.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Room no,(3)digits allowed","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrdob.matcher(sdob.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Valid Date of Birth(dd/mm/yyyy)","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrdob.matcher(sdoj.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Valid Date of JOINING (DD/MM/YYYY)","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrnumber.matcher(spno.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Phone no","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptrnumber.matcher(sppno.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Parent's Phone no","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(!ptr.matcher(ssfee.getText()).matches())
    {JOptionPane.showMessageDialog(this,"Error in Fee(Only numbers)","Message",JOptionPane.ERROR_MESSAGE);return false;}
    if(saddress.getText().isEmpty())
    {JOptionPane.showMessageDialog(this,"Enter Full Address!!","Message",JOptionPane.ERROR_MESSAGE);return false;}

    return true;     
//ptr.matcher(email).matches()
    
}    
